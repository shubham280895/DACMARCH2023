class Q17{
        public static void main(String[] args) {

            int i = 28;
        
            Integer in = new Integer(i);
            byte B1 = in.byteValue();
             System.out.println("Integer instance into byte = "+ B1);

             short sh = in.shortValue();
             System.out.println("Integer instance into short = "+ sh);
             
             int j = in.intValue();
             System.out.println("Integer instance into integer = "+ j);
             
             long l = in.longValue();
             System.out.println("Integer instance into long = "+ l);

             float f = in.floatValue();
             System.out.println("Integer instance into float = "+ f);

             double d = in.doubleValue();
             System.out.println("Integer instance into double = "+ d);

        }
       


 }
     
        